function y0 = gen_y0(N, I0, vacc_rate, start_grade)
    I0 = I0 / N;
    S0 = 1 - I0;
    V0 = S0 * vacc_rate;
    VN = vacc_rate;
    SN = 1 - VN;
    S0 = S0 - V0;
    
    % [ S0_1, S0_2, S0_3, S0_4;
    %   V0_1, V0_2, V0_3, V0_4;
    %   E0_1, E0_2, E0_3, E0_4;
    %   I0_1, I0_2, I0_3, I0_4;
    %   R0_1, R0_2, R0_3, R0_4 ]
    
    if start_grade == 1
        y0 = [S0, SN, SN, SN, ...
              V0, VN, VN, VN, ...
               0, 0, 0, 0, ...
              I0, 0, 0, 0, ...
               0, 0, 0, 0];
    elseif start_grade == 2
        y0 = [SN, S0, SN, SN, ...
              VN, V0, VN, VN, ...
              0,  0, 0, 0, ...
              0, I0, 0, 0,...
              0,  0, 0, 0];
    elseif start_grade == 3
        y0 = [SN, SN, S0, SN, ...
              VN, VN, V0, VN, ...
              0, 0,  0, 0, ...
              0, 0, I0, 0, ...
              0, 0,  0, 0];
    elseif start_grade == 4
        y0 = [SN, SN, SN, S0, ...
              VN, VN, VN, V0, ...
              0, 0, 0,  0, ...
              0, 0, 0, I0, ...
              0, 0, 0,  0];
    end
    
    y0 = y0(:);
end

